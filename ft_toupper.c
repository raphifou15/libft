#include "libft.h"

int	ft_toupper(int c)
{
	if (c == -1)
		return -1;
	unsigned char d = (unsigned char)(c);
	if (d >= 97 && d <= 122)
	{
		d = d - 32;
		return (d);
	}
	return (d);
}
