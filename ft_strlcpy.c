#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	j;

	if (!dst || !src)
		return (0);
	i = 0;
	while (src[i] != '\0')
		i++;
	j = i;
	if (size <= 0)
		return (j);
	i = 0;
	while (src[i] != '\0' && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (j);
}
